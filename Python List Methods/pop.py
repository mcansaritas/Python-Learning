"""The pop() method removes the element at the specified position. The syntax is list.pop(pos)."""

fruits = ['apple', 'banana', 'cherry', 'orange', 'cucumber']  # This is our list of fruits
fruits.pop(1)  # This will pop the index 1 from the list. Banana is removed from the list

print(fruits)
print('-'*30)

fruits.pop(3)  # This will pop the index 3 from the list. Cucumber is removed from the list
print(fruits)
