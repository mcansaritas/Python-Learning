"""The remove() method removes the first occurence of the element with the specified value. The
syntax is list.remove(element)."""

fruits = ['apple', 'banana', 'cherry']  # This is our list of fruits
print(fruits)

print('-'*30)

fruits.remove("banana")  # This will remove the first occurence 'banana' from the list
print(fruits)  # This will print the updated list

