"""The insert() method inserts the specified value at the specified position. The syntax is
list.insert(position, element)."""

fruits = ['apple', 'banana', 'cherry']  # This is our list of fruits
print(fruits)  # This will print our list before insert method
fruits.insert(1, "orange")  # This will insert 'orange' to index 1

print(fruits)  # This will print our list after insert method
