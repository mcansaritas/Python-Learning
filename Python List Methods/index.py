"""The index() method returns the position at the first occurance of the specified value. The
syntax is list.index(element)."""

fruits = ['apple', 'banana', 'cherry']  # This is our list of fruits
cherry_index = fruits.index("cherry")  # This will return the index value of cherry

print(cherry_index)  # This prints out 2 which means 'cherry' is the 3rd element in the list
