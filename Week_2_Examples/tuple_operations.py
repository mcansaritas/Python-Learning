"""There are similar operations that we can do with tuples. Tuples are created with () and
they are sequenced data structures. Let's go and exercise what we have learned."""

my_tuple = (2, "whatever", 3)
print(type(my_tuple))  # It will print class tuple
print(my_tuple[0])  # This will print the first element in tuple.
print(my_tuple[1])  # This will print the second element in tuple.
list_tuple = list(my_tuple)  # This will convert the tuple into a list.
print(list_tuple)
