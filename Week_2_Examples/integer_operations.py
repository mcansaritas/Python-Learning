"""There are some Python integers operation that we can do.Let's write some of the examples
and see that what is the operation."""

print(7+2)  # This is sum operation.
print(7-2)  # This is minus operation.
print(7*2)  # This is multiplication operation.
print(7/2)  # This is divide operation.
print(7%2)  # This is modulo operation.
print(7**2)  # This is power operation.
print(7//2)  # This is python 2 divide operation.
