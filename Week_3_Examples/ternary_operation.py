"""Python has a ternary operator, it is basically a simple if else statement. Let's write a 
couple of codes and see how does it work."""

my_variable = 0

ternary_operation =  "My integer value is 0" if my_variable == 0 else "My integer is not 0"
print(ternary_operation)

my_variable = 1

ternary_operation =  "My integer value is 0" if my_variable == 0 else "My integer is not 0"
print(ternary_operation)
