"""The built-in divmod() method returns a tuple containing ghe quotient and the remainder when
argument1 divident is divided by argument2 (divisor). The syntax is divmod(dividend, divisor)."""

x = divmod(5, 2)
print(x)  # This will print (2, 1) 2 is the quotient and 1 is the remainder

# This will print the quotient and the remainder of 5 divided by 2
