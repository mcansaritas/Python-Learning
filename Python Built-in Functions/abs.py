"""The built-in abs() method returns the absolute value of the specified number. The parameter
 we need to specify is 'n'. Let's write an example."""

complex_number = 12+16j
absolute_value = abs(complex_number)
print(absolute_value)  # It is the magnitude of complex number

"""The abs() method make any negative number to positive, while positive numbers are unaffected.
The absolute value of any number is always positive. For any positive number, the absolute value
is the number itself and for any negative number, the absolute value is (-1) multiplied by the 
negative number."""
