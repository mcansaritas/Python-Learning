"""The count() method returns the number of times a specified value appears in the string.
The syntax is string.count(value, start, end). Value is mandatory, and the start, end parameters
are optional."""

our_text = 'This is our testing to understand the count() method.'  # This is our text
count_counter = our_text.count('count')  # This is a counter for 'count'
print(count_counter)  # This will print out the counter
