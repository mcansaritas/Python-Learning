"""The capitalize() methos returns a string where the first character is upper case, and the
rest is lower case. The syntax is string.capitalize()."""

our_text = 'this is our testing for string capitalize() method.'  # This is our string
print(our_text)  # This is our initial strings and the first character is lower case

print('-'*30)

capitalize_method = our_text.capitalize()  # This will change the first character as upper case
print(capitalize_method)  # This will print out the first character as capitalized
