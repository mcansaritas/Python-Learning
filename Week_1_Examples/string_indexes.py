"""Python strings has indexes. Indexes is based on 0. We are going to see in the examples.
Also, with len output we are able to see the length of the string. We can use it for loops.
We can use negative indexes as well"""

long_string = "Hi, my name is Mertcan and I work for Sky"
print(long_string[0])  # It will print the first letter in string.
print(long_string[-1])  # It will print the last letter in string.
print(long_string[0:10])  # It will print from the first element to 9th element
print(long_string[:-1])  # It will print from the beginning to the end but not the last letter
