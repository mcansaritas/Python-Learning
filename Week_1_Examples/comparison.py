"""We can compare the variables with Python. We need to use "==" for that. Let's write a list
of code and test how does it work."""

variable1 = "1.1.1.1"
variable2 = "1.1.1.2"

print("1.1" in variable1)  # It checks the specified string is in the string.
print("1.1" in variable2)  # It checks the specified string is in the string.
print(variable1 == variable2)  # It checks if they are equal.
